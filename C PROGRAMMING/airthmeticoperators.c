#include<stdio.h>
int main()
    {
    int a = 7;
    int b = 2;
    int c = 19;
    printf("%d,%d\n", a,b);
    //addition rule
    printf("%d\n", a+b+c);
    //multiply rule
    printf("%d\n", a*b*c);
    //substrsction Rule
    printf("%d\n",a-b-c);
    //divide rule
    printf("%d\n",a/b/c);
    return 0;

    }

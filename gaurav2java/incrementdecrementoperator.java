public class incrementdecrementoperator {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x = x + 5;
        System.out.println(x);
        x++; // x++ ka matlab hai x+1 hota hai
        System.out.println(x);
        x--; // x-- ka matlab hai x-1 hota hai
        System.out.println(x);
        System.out.println(++x);
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);
    }

}
